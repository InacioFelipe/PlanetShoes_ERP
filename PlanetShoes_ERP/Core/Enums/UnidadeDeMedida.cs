namespace PlanetShoes.Core.Enums
{
    /// <summary>
    /// Enumeração que representa as unidades de medida utilizadas no sistema.
    /// </summary>
    public enum UnidadeDeMedida
    {
        // Unidades de comprimento
        Milimetro,  // mm
        Centimetro, // cm
        Metro,      // m

        // Unidades de massa
        Miligrama,  // mg
        Grama,      // g
        Quilograma, // kg

        // Unidades de volume
        Mililitro,  // mL
        Litro,      // L

        // Unidades de quantidade
        Pares,        // pr
        Pecas,        // pc
    }
}